# 常量的类型判断
"""
在python中，有一个内置的功能：type
    type主要是用来打印一些变量/常量的类型
"""
print(type(100))  # 输出：int[有符号的整型类型]
print(type(3.14))  # 输出：float[浮点类型]
print(type('双双胖三斤...'))  # 输出：str[字符串]


# 变量的类型判断
num_1 = 10
num_2 = 7.82
str_data = 'hello world'
print(type(num_1))
print(type(num_2))
print(type(str_data))


