"""
如果一个表达式中含有比较运算符, 则这个表达式所得出的结果一般为布尔值
"""

num_1 = 10
num_2 = 15

print(num_1 == num_2)
print(num_1 > num_2)
print(num_1 >= num_2)
print(num_1 < num_2)

# 当前代码有两个条件 == < 只要一个条件成立则结果为True
print(num_1 <= num_2)









